/**
 * 数组中任意两个数的和是否能得出目标数字
 */
public class TwoSum {

    public static void main(String[] args) {

        int a = 20;
        int b = 30;
        System.out.println(a / b);
        System.out.println(a / (float)b);
        System.out.println(a / ((float)b-5));

    }


}
